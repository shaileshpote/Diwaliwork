package logicalprogram;

public class example71 
{
	public static void main(String[]args)
	{
		int num= 4 ;
		
		int count= 0 ;
		
		for(int i= 2 ; i<num ; i++       )
		{
			if (num% i==0)
				
			{
				count++;
			}
	        }
		if (count==1)
		{
			System.out.println ("given number is not prime ");
		}
		
		else
		{
			System.out.println("given number is prime");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
